#include<stdio.h>

void main()
{
		int limit,line,space,iter;
		clrscr();
		printf("How many terms:");
		scanf("%d",&limit);
		for(line=0;line<limit;line++)
		{
			for(space=0;space<=limit-line;space++)
			{
				printf(" ");
			}
			for(iter=0;iter<=line;iter++)
			{
				printf("*");
			}
			for(iter=0;iter<=line-1;iter++)
			{
				 printf("*");
			}
			printf("\n");
		}
getch();
}


